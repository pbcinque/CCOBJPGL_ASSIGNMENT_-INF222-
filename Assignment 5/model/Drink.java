package model;

class Drink {

    private String feel;
    private String taste;

    public String getFeel() {
        return this.feel;
    }

    public void setFeel(String feel) {
        this.feel = feel;
    }

    public String getTaste() {
        return this.taste;
    }

    public void setTaste(String taste) {
        this.taste = taste;
    }
}